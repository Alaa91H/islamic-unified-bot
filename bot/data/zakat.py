def calculate_zakat(amount: float, category: str = "المال") -> dict:
    rate = ZAKAT_RATE
    zakat_due = amount * rate
    return {
        "amount": amount,
        "rate": rate * 100,
        "zakat_due": round(zakat_due, 2),
        "category": category,
    }


def get_nisab_gold(gold_price_per_gram: float) -> float:
    return round(GOLD_NISAB_GRAMS * gold_price_per_gram, 2)


def get_nisab_silver(silver_price_per_gram: float) -> float:
    return round(SILVER_NISAB_GRAMS * silver_price_per_gram, 2)


